    public void generarPDF(String name) throws FileNotFoundException, DocumentException{
        FileOutputStream archivo = new FileOutputStream(name+".pdf");
        Document doc = new Document();
        PdfWriter.getInstance(doc, archivo);
        doc.open();
        
        Paragraph titulo = new Paragraph("Historial de ventas");
        titulo.setAlignment(1);
        doc.add(titulo);
        
        for(int i = 0; i < compras.lista.size(); i++){
            doc.add(new Paragraph("Nombre: "+compras.lista.get(i).getC().getNombre()));
            doc.add(new Paragraph("Apellido: "+compras.lista.get(i).getC().getApellido()));
            doc.add(new Paragraph("Correo: "+compras.lista.get(i).getC().getCorreo()));
            doc.add(new Paragraph("Producto: "+compras.lista.get(i).getP().getNombre()));
            doc.add(new Paragraph("Marca producto: "+compras.lista.get(i).getP().getMarca()));
            doc.add(new Paragraph("Referencia producto: "+compras.lista.get(i).getP().getID()));
            doc.add(new Paragraph("Cantida comprada: "+compras.lista.get(i).getP().getCantidad()));
            doc.add(new Paragraph("Valor compra: $"+compras.lista.get(i).getP().getPrecio()));
            doc.add(new Paragraph("Fecha: "+compras.lista.get(i).getF().getFecha()));
            doc.add(new Paragraph("-------------------------------------------------------------------------------"));
        }
        doc.close();
        JOptionPane.showMessageDialog(this, "PDF generado");
    }
    
    public void abrirPDF(String name){
        try {
            File path = new File(name+".pdf");
            Desktop.getDesktop().open(path);
        } catch (Exception e) {
            System.out.println("Algo salio mal: "+e);
        }
    }
    
    private void generarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_generarActionPerformed
        String input = JOptionPane.showInputDialog("Nombre de su archivo PDF");
        if(input != null && !"".equals(input)){
            try {
                generarPDF(input);
            } catch (FileNotFoundException ex) {
                Logger.getLogger(interVentas.class.getName()).log(Level.SEVERE, null, ex);
            } catch (DocumentException ex) {
                Logger.getLogger(interVentas.class.getName()).log(Level.SEVERE, null, ex);
            }
        }else if("".equals(input)){
            System.out.println("VACIO");
        }else{
            System.out.println("CANCEL");
        }
    }//GEN-LAST:event_generarActionPerformed

