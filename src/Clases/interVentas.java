    public void load(){
//        Producto p = new Producto("AKT", "CHASIS", "CH123",  1561, 2);
        //registro.addProducto(p);
        int j = 0;
        if(modelo.getRowCount() != 0){
            while(j != -1){
                try{
                    modelo.removeRow(modelo.getRowCount() - 1);
                }catch(Exception e){
                    j = -1;
                }
            }
        }
        try{
            for(int i = 0; i < compras.lista.size(); i++){
                String nombre = compras.lista.get(i).getC().getNombre();
                String apellido = compras.lista.get(i).getC().getApellido();
                String produc = compras.lista.get(i).getP().getNombre();
                String marca = compras.lista.get(i).getP().getMarca();
                String cantidad = String.valueOf(compras.lista.get(i).getP().getCantidad());
                String precio = String.valueOf(compras.lista.get(i).getP().getPrecio());
                String fecha = compras.lista.get(i).getF().getFecha();
                
                String [] obj = {nombre, apellido, produc, marca, cantidad, precio, fecha};
                modelo.addRow(obj);
                //System.out.println(registro.listaProducto.get(i).getMarca());
            }
        }catch(Exception e){
            System.out.println("Error: "+ e);
        }
        
    }
    
