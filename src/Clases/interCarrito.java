    public void listaCarrito(){
        int j = 0;
        if(modelo.getRowCount() != 0){
            while(j != -1){
                try{
                    modelo.removeRow(modelo.getRowCount() - 1);
                }catch(Exception e){
                    j = -1;
                }
            }
        }
        try{
            for(int i = 0; i < list.size(); i++){
  
                String marca =  list.get(i).getP().getMarca();
                String tipo = list.get(i).getP().getNombre();
                String ID = list.get(i).getP().getID();
                String valor = String.valueOf(list.get(i).getP().getPrecio());
                String cantidad = String.valueOf(list.get(i).getP().getCantidad());
                this.aux = interCliente.user.getText();
                String [] obj = {marca, tipo, ID, valor, cantidad};

                modelo.addRow(obj);
        }
        }catch(Exception e){
            System.out.println("Error: "+ e);
        }
        
    }
